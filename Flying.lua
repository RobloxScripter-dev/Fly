-- Функция для дешифровки текста методом Виженера
local function vigenereDecrypt(ciphertext, key)
    local keyLen = #key
    local plaintext = ""
    local keyIndex = 1

    for i = 1, #ciphertext do
        local char = ciphertext:sub(i, i)
        local keyChar = key:sub(keyIndex, keyIndex)
        local shift = string.byte(keyChar) - 32  -- Сдвиг ключа относительно пробела

        -- Дешифровка символа
        local decryptedChar = string.char((string.byte(char) - shift - 32) % 95 + 32)
        plaintext = plaintext .. decryptedChar

        -- Переход к следующему символу ключа
        keyIndex = keyIndex % keyLen + 1
    end

    return plaintext
end

-- Функция для создания GUI и обработки ввода
local function createEncryptionGUI()
    -- Создание ScreenGui
    local screenGui = Instance.new("ScreenGui")
    screenGui.Name = "EncryptionGUI"
    screenGui.Parent = game.Players.LocalPlayer:WaitForChild("PlayerGui")

    -- Создание TextBox для ввода ключа
    local textBox = Instance.new("TextBox")
    textBox.Name = "KeyTextBox"
    textBox.Size = UDim2.new(0.5, 0, 0.1, 0)
    textBox.Position = UDim2.new(0.25, 0, 0.4, 0)
    textBox.PlaceholderText = "Enter decryption key..."
    textBox.Parent = screenGui

    -- Создание кнопки подтверждения
    local confirmButton = Instance.new("TextButton")
    confirmButton.Name = "ConfirmButton"
    confirmButton.Size = UDim2.new(0.3, 0, 0.1, 0)
    confirmButton.Position = UDim2.new(0.35, 0, 0.55, 0)
    confirmButton.Text = "Decrypt"
    confirmButton.Parent = screenGui

    -- Функция для выполнения дешифровки и выполнения кода
    local function decryptAndExecute(code)
        local success, errorMsg = loadstring(code)
        if success then
            success()  -- Выполнение расшифрованного Lua-кода

            -- Скрытие элементов GUI после выполнения кода
            textBox.Visible = false
            confirmButton.Visible = false
        else
            print("Error executing decrypted script:", errorMsg)
        end
    end

    -- Обработчик нажатия кнопки подтверждения
    confirmButton.MouseButton1Click:Connect(function()
        local enteredKey = textBox.Text

        -- Если ключ не пустой, выполнить дешифровку и код
        if enteredKey ~= "" then
            local decryptedText = vigenereDecrypt(encryptedText, enteredKey)
            print("Decrypted text:")
            print(decryptedText)

            -- Выполнение расшифрованного текста как скрипта
            decryptAndExecute(decryptedText)
        end
    end)
end

-- Вызов функции для создания GUI
createEncryptionGUI()
